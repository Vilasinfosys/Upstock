package SACHIN;

public class Prime_number {
	public static void main(String[] args) {
		int num=2;
		int count=0;
		for (int i=1;i<=num;i++) {
			if (num%1==0);
			count++;
		}		
	
if(count==2) {
	System.out.println("given no is prime");
	
}
else
{
	System.out.println("given number not prime");
}
}
}